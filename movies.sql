# Movies with the highest revenue in each genre
SELECT m.Title, m.Revenue, m.Genre
FROM movies m
JOIN (
    SELECT Genre, MAX(Revenue) AS Max_Revenue
    FROM movies
    GROUP BY Genre
) g ON m.Genre = g.Genre AND m.Revenue = g.Max_Revenue;


# Movies where the revenue exceeds the average revenue of movies released in the same year
SELECT m.Title, m.Revenue, AVG(m2.Revenue) AS Avg_Revenue
FROM movies m
JOIN movies m2 
ON m.Year = m2.Year
GROUP BY m.Title, m.Revenue
HAVING m.Revenue > AVG(m2.Revenue);
