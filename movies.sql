# Movies with a rating higher than their genre's average rating
SELECT m.Title, m.Rating, g.Genre_Avg_Rating
FROM movies m
JOIN (
	  SELECT Genre, AVG(Rating) AS Genre_Avg_Rating 
      FROM movies 
	  GROUP BY Genre
      ) g
ON m.Genre = g.Genre
WHERE m.Rating > g.Genre_Avg_Rating;


